// Find seals matching a topic string
export function findSealsByTopic(topic) {
  const results = new Set();
  Object.keys(SEAL_KEYWORDS).forEach(keyword => {
    if (topic.includes(keyword)) {
      SEAL_KEYWORDS[keyword].forEach(num => results.add(num));
    }
  });
  return [...results];
}
